# General Price
def price(people):
    print("The price for {0} people is $ {1}." .format(people, people * 10000))

# Early morning discount Price
def price_morning(people):
    print("The morning discount price for {0} people is $ {1}." \
        .format(people, people * 6000))

# Soldier discount Price
def price_soldier(people):
    print("The soldier discount price for {0} people is $ {1}." \
        .format(people, people * 4000))

